package estudos_diversos;

import java.util.Scanner;

public class lerquebradelinha {
	String a,b,c;
	
	public void leratequebra(Scanner scan) {
		a = scan.nextLine();
		b = scan.nextLine();
		c = scan.nextLine();
	}
	
	public void exibir() {
		System.out.println("Dados: ");
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
	}

	
}
